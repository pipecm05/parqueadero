package co.edu.uniquindio.poo;

public class Posicion {
    private Puesto i;
    private Puesto j;
    public Posicion(Puesto i, Puesto j) {
        this.i = i;
        this.j = j;
    }
    public Puesto getI() {
        return i;
    }
    public void setI(Puesto i) {
        this.i = i;
    }
    public Puesto getJ() {
        return j;
    }
    public void setJ(Puesto j) {
        this.j = j;
    }
    
    
}
